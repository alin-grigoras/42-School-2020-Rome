#include "libft.h"

char		*ft_strdup(char *src)
{
	char	*s1;
	int		i;

	i = 0;
	while (src[i] != '\0')
		i++;
	if (!(s1 = malloc(i + 1)))
		return (0);
	i = 0;
	while (src[i] != '\0')
	{
		s1[i] = src[i];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
