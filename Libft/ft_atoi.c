#include "libft.h"

int		ft_atoi(const char *s1)
{
	int i;
	int segno;

	i = 0;
	segno = 1;
	while (((*s1 >= 9 && *s1 <= 13) || (*s1 == ' ')))
		s1++;
	if (*s1 == '-' || *s1 == '+')
	{
		if (*s1 == '-')
			segno *= -1;
		s1++;
	}
	while (*s1 >= '0' && *s1 <= '9')
	{
		i *= 10;
		i += (*s1 - '0');
		s1++;
	}
	if (i < 0 && i > -2147483648)
		return (segno > 0 ? -1 : 0);
	return (i * segno);
}
